    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        session = sessionFactory.openSession();
        String sorgu = "from Ogrenciler where ogrenciid= " + bilgi.getBilgi();
        Query query = session.createQuery(sorgu);
        List <Ogrenciler> list = query.list();
        for(Ogrenciler o : list){
            jTextField7.setText(o.getOgrencino());
            jTextField8.setText(o.getAdsoyad());
            jTextField9.setText(o.getSehir());
            jTextField10.setText(o.getTelno());
        }
        jDialog2.show();
    }//GEN-LAST:event_jButton6ActionPerformed

